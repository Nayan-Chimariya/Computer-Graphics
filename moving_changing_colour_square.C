#include<stdio.h>
#include<conio.h>
#include<graphics.h>
#include<dos.h>
void main()
{
int gd=DETECT,gm,i;
initgraph(&gd,&gm,"C:\\TC\\BGI");
for(i=0;i<400;i++)
{
cleardevice();
rectangle(50+i,50,100+i,100);
setfillstyle(1,RED);
floodfill(51+i,51,255);
delay(5);
}
for(i=0;i<300;i++)
{
cleardevice();
rectangle(450,50+i,500,100+i);
setfillstyle(1,GREEN);
floodfill(451,51+i,255);
delay(5);
}
for(i=0;i<400;i++)
{
cleardevice();
rectangle(450-i,350,500-i,400);
setfillstyle(1,YELLOW);
floodfill(451-i,351,255);
delay(5);
}
for(i=0;i<300;i++)
{
cleardevice();
rectangle(50,350-i,100,400-i);
setfillstyle(1,BLUE);
floodfill(51,351-i,255);
delay(5);
}
getch();
closegraph();

}
